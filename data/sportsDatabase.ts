// Fonction pour obtenir des suggestions de sports
export const getSportSuggestions = (
  query: string,
  limit: number = 10
): SportDB[] => {
  if (!query.trim()) return [];

  const normalizedQuery = query.toLowerCase().trim();

  return SPORTS_DATABASE.filter(
    (sport) =>
      sport.name.toLowerCase().includes(normalizedQuery) ||
      sport.category.toLowerCase().includes(normalizedQuery)
  ).slice(0, limit);
};

// Fonction pour obtenir tous les sports d'une catégorie
export const getSportsByCategory = (category: string): SportDB[] => {
  return SPORTS_DATABASE.filter((sport) => sport.category === category);
};

// Fonction pour obtenir toutes les catégories
export const getCategories = (): string[] => {
  return [...new Set(SPORTS_DATABASE.map((sport) => sport.category))];
};

